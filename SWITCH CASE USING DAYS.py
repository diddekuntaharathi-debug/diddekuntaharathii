#SWITCH CASE
day=int(input("Enter a num:"))
match day:
  case 1:
      print("monday")
  case 2:
      print("Tuesday")
  case 3:
      print("Wednesday")
  case 4:
      print("Thrusday")
  case 5:
      print("Friday")
  case 6:
      print("Saturday")
  case 7:
      print("sunday")
  case _:
      print("Invalid")
    
