# Tuple to List
t = (1, 2, 3)
print("Tuple to List:", list(t))

# String to List
s = "Python"
print("String to List:", list(s))

# Set to List
st = {10, 20, 30}
print("Set to List:", list(st))

# Range to List
r = range(5)
print("Range to List:", list(r))

# Dictionary to List (keys will be converted)
d = {"a":1, "b":2, "c":3}
print("Dictionary to List:", list(d))
